package com.schoolofnet.SparkApp.beans;

public class MyBean {
	private String name;
	private Integer age;
	
	public MyBean(String name) {
		this.name = name;
	}
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}
}
